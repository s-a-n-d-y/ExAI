def calculate_error(T, T_hat):
    #   Calculate error
    diff = T-T_hat
    n_samples = diff.shape[0]
    #print(n_samples)
    diff_norm =  np.linalg.norm(diff)
    error = diff_norm*diff_norm/n_samples
    return error


